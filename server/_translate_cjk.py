def translate_line(line):
    """Replace all known Chinese phrases in a line with English translations."""
    # Sort by length descending to match longest phrases first
    for zh, en in sorted(TRANSLATIONS.items(), key=lambda x: -len(x[0])):
        if zh in line:
            line = line.replace(zh, en)
    return line

def process_file(filepath, dry_run=False):
    """Process a single Go file, translating CJK-related text to English where mapped."""
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    if not CJK_RE.search(content):
        return 0

    new_content = []
    changes = 0
    for line in content.split('\n'):
        if CJK_RE.search(line):
            new_line = translate_line(line)
            if new_line != line:
                changes += 1
            new_content.append(new_line)
        else:
            new_content.append(line)

    if changes > 0 and not dry_run:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write('\n'.join(new_content))

    return changes


def parse_args():
    """--dry-run, -n, -, --dry → preview only; lone '-' is NOT 'write' mode."""
    argv = sys.argv[1:]
    if not argv:
        return False
    dry = False
    for a in argv:
        if a in ('--dry-run', '-n', '--dry', '-'):
            dry = True
        elif a in ('-h', '--help'):
            print(__doc__)
            sys.exit(0)
    return dry


def main():
    dry_run = parse_args()

    dirs = [
        'api/',
        'cmd/',
        'config/',
        'core/',
        'docs/',
        'global/',
        'initialize/',
        'mcp/',
        'middleware/',
        'model/',
        'plugin/',
        'router/',
        'service/',
        'source/',
        'task/',
        'utils/',
    ]

    total_files = 0
    total_changes = 0

    root_go = ["main.go"]
    for fname in root_go:
        filepath = os.path.join(os.path.dirname(os.path.abspath(__file__)), fname)
        if not os.path.isfile(filepath):
            filepath = fname
        if not os.path.isfile(filepath):
            continue
        changes = process_file(filepath, dry_run)
        if changes > 0:
            total_files += 1
            total_changes += changes
            if dry_run:
                print(f"  [DRY] {filepath}: {changes} lines would change")

    for d in dirs:
        if not os.path.isdir(d):
            continue
        for root, _, files in os.walk(d):
            for fname in sorted(files):
                if not fname.endswith('.go'):
                    continue
                filepath = os.path.join(root, fname)
                changes = process_file(filepath, dry_run)
                if changes > 0:
                    total_files += 1
                    total_changes += changes
                    if dry_run:
                        print(f"  [DRY] {filepath}: {changes} lines would change")

    mode = "DRY RUN" if dry_run else "APPLIED"
    print(f"\n{mode}: {total_files} files, {total_changes} lines changed")

    remaining = count_lines_with_cjk_related(dirs, root_go_files=root_go)
    print(f"Remaining lines with CJK-related characters: {remaining}")


def count_lines_with_cjk_related(dirs, root_go_files=None):
    """Count lines still containing Han/Hiragana/Katakana/Hangul/CJK punct/fullwidth (same class as CJK_RE)."""
    n = 0
    root_go_files = root_go_files or []
    base = os.path.dirname(os.path.abspath(__file__))
    for fname in root_go_files:
        path = os.path.join(base, fname)
        if not os.path.isfile(path):
            path = fname
        if not os.path.isfile(path):
            continue
        try:
            with open(path, 'r', encoding='utf-8') as f:
                for line in f:
                    if CJK_RE.search(line):
                        n += 1
        except OSError:
            continue
    for d in dirs:
        if not os.path.isdir(d):
            continue
        for root, _, files in os.walk(d):
            for fname in files:
                if not fname.endswith('.go'):
                    continue
                path = os.path.join(root, fname)
                try:
                    with open(path, 'r', encoding='utf-8') as f:
                        for line in f:
                            if CJK_RE.search(line):
                                n += 1
                except OSError:
                    continue
    return n

if __name__ == '__main__':
    main()
